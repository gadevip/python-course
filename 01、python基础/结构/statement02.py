'''
练习：
	需求:
	消消乐
	lv11v2
	lv1:免费玩随便玩
	1v2:充值买道具继续玩
'''
print('*'*10,'欢迎来到消消乐','*'*10)
level = input('请输入你的级别(1v1, lv2):')
if level=='1v1':
	print('免费玩随便玩')
else:
	print('已经进入付费级别，充值继续玩')
	money = int(input('请充值(必须是100的倍数):'))
	#if 语句是允许嵌套,注意缩进问题
	if money%100 == 0 and money>0:
		print('充值成功!充值金额是:',money)
	else:
		print('充值失败，充值金额必须是100的倍数!')